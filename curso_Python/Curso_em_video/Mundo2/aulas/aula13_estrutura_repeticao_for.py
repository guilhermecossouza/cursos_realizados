# o range vai executar o numero - 1
# se caso quiser que mostre os seis oi tenho duas opções, colocar de 1 ao 7 ou de 0 a 6
# a contagen do range não começa do 0
for c in range(1, 6):
    print("oi")
print("fim")
print("-="*30)
for c in range(0, 6):
    print(c)
print("-="*30)
# realizar contagem regressiva
for c in range(0, 6, - 1):
    print(c)
print("-="*30)
# pulando de dois em dois
for c in range(0, 10, 2):
    print(c)
