const caculadora = {
    somar: (num1, num2) => {
        return num1 + num2
    },
    subitrair: (num1, num2) => {
        return num1 - num2
    },
    multiplicar: (num1, num2) => {
        return num1 * num2
    },
    dividir: (num1, num2) => {
        return num1 / num2
    }
}

console.log(caculadora.somar(1,2))
console.log(caculadora.subitrair(1,2))
console.log(caculadora.multiplicar(1,2))
console.log(caculadora.dividir(1,2))


